package src.boj.baiscs;
import java.util.*;

public class Sum4 {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        while(sc.hasNext()) {
            int n = sc.nextInt();
            int m = sc.nextInt();
            System.out.println(n+m);
        }
        sc.close();


    }
}
